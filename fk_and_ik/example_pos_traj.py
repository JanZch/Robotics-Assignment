import rclpy
import numpy as np
from rclpy.node import Node
from trajectory_msgs.msg import JointTrajectory, JointTrajectoryPoint

class ExampleTraj(Node):
    
    def __init__(self, data):
        super().__init__('example_trajectory')
        self.data = data
        self.current_index = 0  # Start at the first position
        
        self._publisher = self.create_publisher(JointTrajectory, 'joint_cmds', 10)
        
        # Trigger the callback every 0.1 seconds
        timer_period = 0.1  
        self._timer = self.create_timer(timer_period, self.timer_callback_cycle)

    def timer_callback_cycle(self):
        # 1. Get the current index and wrap around if at the end of the array
        index = self.current_index % len(self.data)
        self.current_index += 1
    
        # 2. Extract the row, convert to radians, and append the 0.0 joint
        current_joint_state = self.data[index]
        joint_positions = np.append(current_joint_state, 0.0)

        # 3. Create the trajectory point
        point = JointTrajectoryPoint()
        point.positions = list(joint_positions)

        # 4. Build and publish the message
        msg = JointTrajectory()
        msg.header.stamp = self.get_clock().now().to_msg()
        msg.points = [point]
    
        self._publisher.publish(msg)
        
        # Optional: Print to terminal to verify it's working
        self.get_logger().info(f"Published index {index}")

